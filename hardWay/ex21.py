# coding: utf-8

def add(a, b):
    print('ADDING %d + %d' % (a, b))
    return a + b


print(add(1, 3))


def subtract(a, b):
    print('SUBTRACTING %d - %d' % (a, b))
    return a - b


print(subtract(5, 3))


def multiply(a, b):
    print('MULTIPLYING %d * %d' % (a, b))
    return a * b


print(multiply(8, 2))


def divide(a, b):
    print('DIVIDING %d / %d' % (a, b))
    return a / b


print(divide(6, 2))
